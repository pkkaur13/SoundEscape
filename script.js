  // Get the radio buttons and the current rating element
const ratingRadios = document.querySelectorAll('input[name="ratingRadio"]');
const currentRatingElement = document.querySelector('.current-rating');

// Add event listener to the radio buttons
ratingRadios.forEach((radio) => {
  radio.addEventListener('change', updateRating);
});

// Function to update the current rating
function updateRating() {
  let selectedRating = 0;
  
  // Find the selected rating
  ratingRadios.forEach((radio) => {
    if (radio.checked) {
      selectedRating = 6-parseInt(radio.value);
    }
  });
  
  // Update the current rating text
  currentRatingElement.textContent = selectedRating + ' of 5';
}
